def merge_headers(base_headers: Dict[str, str], additional_headers: Dict[str, str]) -> Dict[str, str]:
    """
    Merge two sets of headers, with additional_headers taking priority.

    Args:
        base_headers: Base headers to start with
        additional_headers: Additional headers to add or override

    Returns:
        Dict[str, str]: Merged headers
    """
    result = base_headers.copy()
    result.update(additional_headers)
    return result


def get_xhr_headers(referrer: Optional[str] = None) -> Dict[str, str]:
    """
    Get headers suitable for XHR/AJAX requests.

    Args:
        referrer: Optional referrer URL

    Returns:
        Dict[str, str]: Dictionary of HTTP headers for XHR requests
    """
    headers = {
        "User-Agent": get_random_user_agent(),
        "Accept": "application/json, text/javascript, */*; q=0.01",
        "Accept-Language": "en-US,en;q=0.5",
        "Accept-Encoding": "gzip, deflate, br",
        "X-Requested-With": "XMLHttpRequest",
        "Connection": "keep-alive",
        "Sec-Fetch-Dest": "empty",
        "Sec-Fetch-Mode": "cors",
        "Sec-Fetch-Site": "same-origin"
    }

    if referrer:
        headers["Referer"] = referrer

    return headers